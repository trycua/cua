use cua_driver_contract::{manifest, ContractManifest, ToolContract};
use serde_json::Value;
use std::collections::BTreeSet;
use std::fmt::Write as _;
use std::fs;
use std::path::{Path, PathBuf};

fn main() {
    if let Err(error) = run() {
        eprintln!("cua-contract-gen: {error}");
        std::process::exit(1);
    }
}

fn run() -> Result<(), String> {
    let args: Vec<String> = std::env::args().skip(1).collect();
    let check = args.iter().any(|arg| arg == "--check");
    let target = args
        .iter()
        .find(|arg| !arg.starts_with('-'))
        .map(String::as_str)
        .unwrap_or("all");
    if !matches!(target, "all" | "manifest" | "clients") {
        return Err("usage: cua-contract-gen [all|manifest|clients] [--check]".into());
    }

    let root = driver_root()?;
    let manifest = manifest();
    if matches!(target, "all" | "manifest") {
        let mut contents = serde_json::to_string_pretty(&manifest).map_err(|e| e.to_string())?;
        contents.push('\n');
        write_or_check(&root.join("contract/manifest.json"), &contents, check)?;
    }
    if matches!(target, "all" | "clients") {
        write_or_check(
            &root.join("clients/python/src/cua_driver_client/_generated.py"),
            &generate_python(&manifest)?,
            check,
        )?;
        write_or_check(
            &root.join("clients/typescript/src/generated.ts"),
            &generate_typescript(&manifest)?,
            check,
        )?;
    }
    Ok(())
}

fn driver_root() -> Result<PathBuf, String> {
    let crate_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    crate_dir
        .parent()
        .and_then(Path::parent)
        .and_then(Path::parent)
        .map(Path::to_path_buf)
        .ok_or_else(|| "could not resolve libs/cua-driver root".into())
}

fn write_or_check(path: &Path, expected: &str, check: bool) -> Result<(), String> {
    if check {
        let actual = fs::read_to_string(path)
            .map_err(|error| format!("{} is missing: {error}", path.display()))?;
        if actual != expected {
            return Err(format!(
                "{} is stale; run `cargo run -p cua-driver-contract --bin cua-contract-gen -- all`",
                path.display()
            ));
        }
        println!("checked {}", path.display());
        return Ok(());
    }
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).map_err(|error| error.to_string())?;
    }
    fs::write(path, expected).map_err(|error| error.to_string())?;
    println!("generated {}", path.display());
    Ok(())
}

fn generate_python(manifest: &ContractManifest) -> Result<String, String> {
    let mut out = format!(
        "# Generated by cua-contract-gen; do not edit by hand.\n\
from __future__ import annotations\n\n\
from dataclasses import dataclass\n\
from typing import Any, Literal\n\n\
CONTRACT_VERSION = \"{}\"\n\
MCP_PROTOCOL_VERSION = \"{}\"\n\n",
        manifest.contract_version, manifest.mcp_protocol_version
    );
    for tool in &manifest.tools {
        let class_name = format!("{}Args", pascal_case(&tool.name));
        let properties = ordered_properties(tool)?;
        let required = required_set(tool);
        writeln!(out, "@dataclass(frozen=True)").unwrap();
        writeln!(out, "class {class_name}:").unwrap();
        for (name, schema) in properties
            .iter()
            .filter(|(name, _)| required.contains(name))
        {
            writeln!(out, "    {name}: {}", python_type(schema)).unwrap();
        }
        for (name, schema) in properties
            .iter()
            .filter(|(name, _)| !required.contains(name))
        {
            writeln!(out, "    {name}: {} | None = None", python_type(schema)).unwrap();
        }
        writeln!(out, "\n    def to_arguments(self) -> dict[str, Any]:").unwrap();
        writeln!(out, "        arguments: dict[str, Any] = {{}}").unwrap();
        for (name, _) in &properties {
            if required.contains(name) {
                writeln!(out, "        arguments[\"{name}\"] = self.{name}").unwrap();
            } else {
                writeln!(out, "        if self.{name} is not None:").unwrap();
                writeln!(out, "            arguments[\"{name}\"] = self.{name}").unwrap();
            }
        }
        writeln!(out, "        return arguments\n\n").unwrap();
    }
    out.push_str("class GeneratedClientMixin:\n");
    out.push_str("    def call_tool(self, name: str, arguments: dict[str, Any]) -> Any:\n");
    out.push_str("        raise NotImplementedError\n\n");
    for tool in &manifest.tools {
        let class_name = format!("{}Args", pascal_case(&tool.name));
        writeln!(
            out,
            "    def {}(self, arguments: {class_name}) -> Any:\n        return self.call_tool(\"{}\", arguments.to_arguments())\n",
            tool.name, tool.name
        )
        .unwrap();
    }
    normalize_eof(&mut out);
    Ok(out)
}

fn generate_typescript(manifest: &ContractManifest) -> Result<String, String> {
    let mut out = format!(
        "// Generated by cua-contract-gen; do not edit by hand.\n\n\
import type {{ ToolResult }} from \"./result.js\"\n\n\
export const CONTRACT_VERSION = \"{}\" as const\n\
export const MCP_PROTOCOL_VERSION = \"{}\" as const\n\n",
        manifest.contract_version, manifest.mcp_protocol_version
    );
    for tool in &manifest.tools {
        let type_name = format!("{}Args", pascal_case(&tool.name));
        let properties = ordered_properties(tool)?;
        let required = required_set(tool);
        writeln!(out, "export interface {type_name} {{").unwrap();
        for (wire_name, schema) in &properties {
            let name = camel_case(wire_name);
            let optional = if required.contains(wire_name) {
                ""
            } else {
                "?"
            };
            writeln!(out, "  {name}{optional}: {}", typescript_type(schema)).unwrap();
        }
        writeln!(out, "}}\n").unwrap();
        writeln!(
            out,
            "function {}ToWire(input: {type_name}): Record<string, unknown> {{",
            camel_case(&tool.name)
        )
        .unwrap();
        writeln!(out, "  const output: Record<string, unknown> = {{}}").unwrap();
        for (wire_name, _) in &properties {
            let name = camel_case(wire_name);
            if required.contains(wire_name) {
                writeln!(out, "  output.{wire_name} = input.{name}").unwrap();
            } else {
                writeln!(
                    out,
                    "  if (input.{name} !== undefined) output.{wire_name} = input.{name}"
                )
                .unwrap();
            }
        }
        writeln!(out, "  return output\n}}\n").unwrap();
    }
    out.push_str("export abstract class GeneratedClient {\n");
    out.push_str("  abstract callTool(name: string, input?: Record<string, unknown>): Promise<ToolResult>\n\n");
    for tool in &manifest.tools {
        let type_name = format!("{}Args", pascal_case(&tool.name));
        writeln!(
            out,
            "  {}(input: {type_name}): Promise<ToolResult> {{\n    return this.callTool(\"{}\", {}ToWire(input))\n  }}\n",
            camel_case(&tool.name),
            tool.name,
            camel_case(&tool.name)
        )
        .unwrap();
    }
    out.push_str("}\n");
    normalize_eof(&mut out);
    Ok(out)
}

fn normalize_eof(output: &mut String) {
    let content_len = output.trim_end().len();
    output.truncate(content_len);
    output.push('\n');
}

fn ordered_properties(tool: &ToolContract) -> Result<Vec<(String, Value)>, String> {
    let properties = tool.input_schema["properties"]
        .as_object()
        .ok_or_else(|| format!("{}.input_schema.properties is not an object", tool.name))?;
    let mut ordered = Vec::new();
    for name in required_names(tool) {
        if let Some(schema) = properties.get(&name) {
            ordered.push((name, schema.clone()));
        }
    }
    let required = required_set(tool);
    ordered.extend(
        properties
            .iter()
            .filter(|(name, _)| !required.contains(*name))
            .map(|(name, schema)| (name.clone(), schema.clone())),
    );
    Ok(ordered)
}

fn required_names(tool: &ToolContract) -> Vec<String> {
    tool.input_schema["required"]
        .as_array()
        .into_iter()
        .flatten()
        .filter_map(Value::as_str)
        .map(str::to_owned)
        .collect()
}

fn required_set(tool: &ToolContract) -> BTreeSet<String> {
    required_names(tool).into_iter().collect()
}

fn python_type(schema: &Value) -> String {
    if let Some(values) = schema["enum"].as_array() {
        let values = values
            .iter()
            .filter_map(Value::as_str)
            .map(|value| format!("\"{value}\""))
            .collect::<Vec<_>>()
            .join(", ");
        return format!("Literal[{values}]");
    }
    match schema["type"].as_str() {
        Some("string") => "str".into(),
        Some("integer") => "int".into(),
        Some("number") => "float".into(),
        Some("boolean") => "bool".into(),
        Some("array") => "list[Any]".into(),
        _ => "dict[str, Any]".into(),
    }
}

fn typescript_type(schema: &Value) -> String {
    if let Some(values) = schema["enum"].as_array() {
        return values
            .iter()
            .filter_map(Value::as_str)
            .map(|value| format!("\"{value}\""))
            .collect::<Vec<_>>()
            .join(" | ");
    }
    match schema["type"].as_str() {
        Some("string") => "string".into(),
        Some("integer" | "number") => "number".into(),
        Some("boolean") => "boolean".into(),
        Some("array") => "unknown[]".into(),
        _ => "Record<string, unknown>".into(),
    }
}

fn pascal_case(value: &str) -> String {
    value
        .split('_')
        .map(|part| {
            let mut chars = part.chars();
            chars
                .next()
                .map(|first| first.to_uppercase().collect::<String>() + chars.as_str())
                .unwrap_or_default()
        })
        .collect()
}

fn camel_case(value: &str) -> String {
    let pascal = pascal_case(value);
    let mut chars = pascal.chars();
    chars
        .next()
        .map(|first| first.to_lowercase().collect::<String>() + chars.as_str())
        .unwrap_or_default()
}
