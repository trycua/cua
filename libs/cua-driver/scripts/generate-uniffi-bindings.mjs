#!/usr/bin/env node

import { spawnSync } from "node:child_process"
import {
  existsSync,
  lstatSync,
  mkdirSync,
  mkdtempSync,
  readFileSync,
  readdirSync,
  renameSync,
  rmSync,
  unlinkSync,
  writeFileSync,
} from "node:fs"
import { tmpdir } from "node:os"
import { dirname, isAbsolute, join, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const scriptDirectory = dirname(fileURLToPath(import.meta.url))
const driverRoot = resolve(scriptDirectory, "..")
const rustRoot = join(driverRoot, "rust")
const pythonRoot = join(driverRoot, "python")
const typescriptRoot = join(driverRoot, "typescript")
const check = process.argv.includes("--check")

function run(command, args, cwd) {
  const result = spawnSync(command, args, { cwd, stdio: "inherit" })
  if (result.error) throw result.error
  if (result.status !== 0) {
    throw new Error(`${command} exited with status ${result.status}`)
  }
}

function nativeLibraryPath() {
  const file =
    process.platform === "darwin"
      ? "libcua_driver_sdk.dylib"
      : process.platform === "win32"
        ? "cua_driver_sdk.dll"
        : "libcua_driver_sdk.so"
  return join(rustRoot, "target", "release", file)
}

function normalizeWhitespace(source) {
  const output = source
    .replaceAll("\r\n", "\n")
    .split("\n")
    .map((line) => line.replace(/[ \t]+$/u, ""))
    .join("\n")
  return output.endsWith("\n") ? output : `${output}\n`
}

function normalizeTypeScript(name, source) {
  let output = normalizeWhitespace(source)
  // NodeNext requires emitted relative ESM imports to carry their .js suffix.
  output = output.replace(
    /(from\s+["']\.\/[A-Za-z0-9_-]+)(["'])/g,
    "$1.js$2",
  )
  if (name.endsWith("-ffi.ts")) {
    const needle = 'import lib from "@ubjs/node";'
    const matches = output.split(needle).length - 1
    if (matches !== 1) {
      throw new Error(`expected one Node runtime import in ${name}, found ${matches}`)
    }
    output = output.replace(needle, 'import lib from "./node-runtime.js";')
  }
  if (name === "cua_driver_contract-ffi.ts") {
    const needle = 'crateName: "cua_driver_contract"'
    const matches = output.split(needle).length - 1
    if (matches !== 1) {
      throw new Error(`expected one contract Node library selector, found ${matches}`)
    }
    // The contract namespace is linked into the SDK cdylib. UBRN 0.31.0-3
    // cannot configure a shared cdylib name for an external component.
    output = output.replace(needle, 'crateName: "cua_driver_sdk"')
  }
  return output
}

function inventoryContents(paths) {
  return [
    "# Generated by scripts/generate-uniffi-bindings.mjs; one owned path per line.",
    ...paths.slice().sort(),
    "",
  ].join("\n")
}

function assertOwnedPath(path) {
  const parts = path.split(/[\\/]/)
  if (
    isAbsolute(path) ||
    parts.some((part) => part === "" || part === "." || part === "..")
  ) {
    throw new Error(`invalid generated ownership path: ${path}`)
  }
  return path
}

function applyGroup(root, inventoryName, files) {
  const inventory = join(root, inventoryName)
  const previous = existsSync(inventory)
    ? readFileSync(inventory, "utf8")
        .split("\n")
        .filter((line) => line && !line.startsWith("#"))
        .map(assertOwnedPath)
    : []
  const expected = Object.keys(files).map(assertOwnedPath).sort()
  const planned = { ...files, [inventoryName]: inventoryContents(expected) }

  if (check) {
    const differences = previous.filter((path) => !expected.includes(path))
    for (const [path, contents] of Object.entries(planned)) {
      const destination = join(root, path)
      if (!existsSync(destination) || readFileSync(destination, "utf8") !== contents) {
        differences.push(path)
      }
    }
    if (differences.length) {
      throw new Error(`stale UniFFI generated files under ${root}: ${differences.join(", ")}`)
    }
    return
  }

  for (const stale of previous.filter((path) => !expected.includes(path))) {
    const destination = join(root, stale)
    if (existsSync(destination)) {
      if (lstatSync(destination).isSymbolicLink()) {
        throw new Error(`refusing to prune generated symlink ${destination}`)
      }
      unlinkSync(destination)
    }
  }
  for (const [path, contents] of Object.entries(planned)) {
    const destination = join(root, path)
    if (existsSync(destination) && lstatSync(destination).isSymbolicLink()) {
      throw new Error(`refusing to overwrite generated symlink ${destination}`)
    }
    mkdirSync(dirname(destination), { recursive: true })
    const temporary = `${destination}.tmp.${process.pid}`
    writeFileSync(temporary, contents)
    renameSync(temporary, destination)
  }
}

const temporaryRoot = mkdtempSync(join(tmpdir(), "cua-driver-uniffi-"))
try {
  run("cargo", ["build", "--locked", "--release", "-p", "cua-driver-sdk"], rustRoot)
  const library = nativeLibraryPath()
  if (!existsSync(library)) throw new Error(`missing built SDK library ${library}`)

  const pythonOutput = join(temporaryRoot, "python")
  run(
    "cargo",
    [
      "run",
      "--locked",
      "-p",
      "cua-driver-bindgen",
      "--bin",
      "cua-driver-bindgen",
      "--",
      "generate",
      "--library",
      library,
      "--language",
      "python",
      "--out-dir",
      pythonOutput,
      "--no-format",
    ],
    rustRoot,
  )

  const typescriptOutput = join(temporaryRoot, "typescript")
  const ubrn = join(
    typescriptRoot,
    "node_modules",
    ".bin",
    process.platform === "win32" ? "ubrn.cmd" : "ubrn",
  )
  if (!existsSync(ubrn)) {
    throw new Error("missing pinned UBRN generator; run npm ci in libs/cua-driver/typescript")
  }
  run(
    ubrn,
    [
      "generate",
      "napi",
      "bindings",
      "--library",
      library,
      "--ts-dir",
      typescriptOutput,
      "--lib-package-base",
      "@trycua/cua-driver",
      "--lib-node-triple",
      "--no-format",
    ],
    rustRoot,
  )

  applyGroup(pythonRoot, ".cua-driver-uniffi-generated-files", {
    "src/cua_driver/_native.py": normalizeWhitespace(
      readFileSync(join(pythonOutput, "cua_driver_sdk.py"), "utf8"),
    ),
    "src/cua_driver/_native_contract.py": normalizeWhitespace(
      readFileSync(join(pythonOutput, "cua_driver_contract.py"), "utf8"),
    ),
  })

  const typescriptFiles = {}
  for (const name of readdirSync(typescriptOutput).filter((name) => name.endsWith(".ts"))) {
    typescriptFiles[`src/native/${name}`] = normalizeTypeScript(
      name,
      readFileSync(join(typescriptOutput, name), "utf8"),
    )
  }
  applyGroup(typescriptRoot, ".cua-driver-uniffi-generated-files", typescriptFiles)
  console.log(check ? "UniFFI bindings are up to date." : "Generated UniFFI bindings.")
} finally {
  rmSync(temporaryRoot, { recursive: true, force: true })
}
