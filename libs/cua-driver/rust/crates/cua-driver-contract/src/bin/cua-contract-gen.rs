use cua_driver_contract::{manifest, ContractManifest, ToolContract};
use serde_json::Value;
use std::collections::{BTreeMap, BTreeSet};
use std::fmt::Write as _;
use std::fs;
use std::path::{Component, Path, PathBuf};

const OWNERSHIP_FILE: &str = ".cua-driver-generated-files";

#[derive(Debug)]
struct GeneratedFile {
    path: PathBuf,
    contents: String,
}

#[derive(Debug)]
struct OwnershipGroup {
    root: PathBuf,
    files: BTreeMap<PathBuf, String>,
}

fn main() {
    if let Err(error) = run() {
        eprintln!("cua-contract-gen: {error}");
        std::process::exit(1);
    }
}

fn run() -> Result<(), String> {
    let args: Vec<String> = std::env::args().skip(1).collect();
    let check = args.iter().any(|arg| arg == "--check");
    let target = args
        .iter()
        .find(|arg| !arg.starts_with('-'))
        .map(String::as_str)
        .unwrap_or("all");
    if !matches!(target, "all" | "manifest" | "sdks") {
        return Err("usage: cua-contract-gen [all|manifest|sdks] [--check]".into());
    }

    let root = driver_root()?;
    let contract = manifest();
    let mut files = Vec::new();
    if matches!(target, "all" | "manifest") {
        let mut contents = serde_json::to_string_pretty(&contract).map_err(|e| e.to_string())?;
        contents.push('\n');
        files.push(GeneratedFile {
            path: root.join("contract/manifest.json"),
            contents,
        });
    }
    if matches!(target, "all" | "sdks") {
        files.push(GeneratedFile {
            path: root.join("python/src/cua_driver/_generated.py"),
            contents: generate_python(&contract)?,
        });
        files.push(GeneratedFile {
            path: root.join("typescript/src/generated.ts"),
            contents: generate_typescript(&contract)?,
        });
    }
    apply_plan(&root, files, check)?;
    Ok(())
}

fn driver_root() -> Result<PathBuf, String> {
    let crate_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    crate_dir
        .parent()
        .and_then(Path::parent)
        .and_then(Path::parent)
        .map(Path::to_path_buf)
        .ok_or_else(|| "could not resolve libs/cua-driver root".into())
}

fn apply_plan(root: &Path, files: Vec<GeneratedFile>, check: bool) -> Result<(), String> {
    let mut ordinary = Vec::new();
    let mut groups = BTreeMap::<PathBuf, OwnershipGroup>::new();
    for file in files {
        if let Some(owner) = sdk_owner(root, &file.path) {
            let relative = file
                .path
                .strip_prefix(&owner)
                .map_err(|error| error.to_string())?
                .to_path_buf();
            validate_relative(&relative)?;
            groups
                .entry(owner.clone())
                .or_insert_with(|| OwnershipGroup {
                    root: owner,
                    files: BTreeMap::new(),
                })
                .files
                .insert(relative, file.contents);
        } else {
            ordinary.push(file);
        }
    }

    // Rendering and validation finish before any mutation. SDK packages
    // additionally carry an ownership inventory so stale generated files are
    // detected (or pruned) without touching hand-written files.
    for group in groups.values_mut() {
        let inventory = ownership_contents(group.files.keys());
        group.files.insert(PathBuf::from(OWNERSHIP_FILE), inventory);
    }

    for file in ordinary {
        write_or_check(&file.path, &file.contents, check)?;
    }
    for group in groups.values() {
        apply_group(group, check)?;
    }
    Ok(())
}

fn sdk_owner(root: &Path, path: &Path) -> Option<PathBuf> {
    [root.join("python"), root.join("typescript")]
        .into_iter()
        .find(|candidate| path.starts_with(candidate))
}

fn validate_relative(path: &Path) -> Result<(), String> {
    if path.as_os_str().is_empty()
        || path.is_absolute()
        || path
            .components()
            .any(|part| !matches!(part, Component::Normal(_)))
    {
        return Err(format!("unsafe generated path: {}", path.display()));
    }
    Ok(())
}

fn ownership_contents<'a>(paths: impl Iterator<Item = &'a PathBuf>) -> String {
    let mut output = String::from("# Generated by cua-contract-gen; one owned path per line.\n");
    for path in paths {
        writeln!(output, "{}", path.to_string_lossy().replace('\\', "/")).unwrap();
    }
    output
}

fn owned_paths(group: &OwnershipGroup) -> Result<BTreeSet<PathBuf>, String> {
    let inventory = group.root.join(OWNERSHIP_FILE);
    let Ok(contents) = fs::read_to_string(&inventory) else {
        return Ok(BTreeSet::new());
    };
    contents
        .lines()
        .filter(|line| !line.is_empty() && !line.starts_with('#'))
        .map(|line| {
            let path = PathBuf::from(line);
            validate_relative(&path)?;
            Ok(path)
        })
        .collect()
}

fn apply_group(group: &OwnershipGroup, check: bool) -> Result<(), String> {
    let previous = owned_paths(group)?;
    let expected: BTreeSet<_> = group
        .files
        .keys()
        .filter(|path| path.as_os_str() != OWNERSHIP_FILE)
        .cloned()
        .collect();
    let stale: Vec<_> = previous.difference(&expected).cloned().collect();
    if check && !stale.is_empty() {
        return Err(format!(
            "{} owns stale generated paths: {}",
            group.root.display(),
            stale
                .iter()
                .map(|path| path.display().to_string())
                .collect::<Vec<_>>()
                .join(", ")
        ));
    }
    for (relative, contents) in &group.files {
        write_or_check(&group.root.join(relative), contents, check)?;
    }
    if !check {
        for relative in stale {
            let path = group.root.join(relative);
            if path.is_symlink() {
                return Err(format!(
                    "refusing to prune generated symlink {}",
                    path.display()
                ));
            }
            if path.exists() {
                fs::remove_file(&path).map_err(|error| format!("{}: {error}", path.display()))?;
                println!("pruned {}", path.display());
            }
        }
    }
    Ok(())
}

fn write_or_check(path: &Path, expected: &str, check: bool) -> Result<(), String> {
    if check {
        let actual = fs::read_to_string(path)
            .map_err(|error| format!("{} is missing: {error}", path.display()))?;
        if actual != expected {
            return Err(format!(
                "{} is stale; run `cargo run -p cua-driver-contract --bin cua-contract-gen -- all`",
                path.display()
            ));
        }
        println!("checked {}", path.display());
        return Ok(());
    }
    if path.is_symlink() {
        return Err(format!(
            "refusing to overwrite generated symlink {}",
            path.display()
        ));
    }
    let parent = path
        .parent()
        .ok_or_else(|| format!("{} has no parent", path.display()))?;
    fs::create_dir_all(parent).map_err(|error| error.to_string())?;
    let temporary = parent.join(format!(
        ".{}.tmp.{}",
        path.file_name().unwrap_or_default().to_string_lossy(),
        std::process::id()
    ));
    fs::write(&temporary, expected).map_err(|error| error.to_string())?;
    if path.exists() {
        let backup = parent.join(format!(
            ".{}.bak.{}",
            path.file_name().unwrap_or_default().to_string_lossy(),
            std::process::id()
        ));
        fs::rename(path, &backup).map_err(|error| error.to_string())?;
        if let Err(error) = fs::rename(&temporary, path) {
            let _ = fs::rename(&backup, path);
            let _ = fs::remove_file(&temporary);
            return Err(error.to_string());
        }
        fs::remove_file(backup).map_err(|error| error.to_string())?;
    } else {
        fs::rename(&temporary, path).map_err(|error| error.to_string())?;
    }
    println!("generated {}", path.display());
    Ok(())
}

fn generate_python(manifest: &ContractManifest) -> Result<String, String> {
    let mut out = format!(
        "# Generated by cua-contract-gen; do not edit by hand.\n\
from __future__ import annotations\n\n\
from dataclasses import dataclass\n\
from typing import Any, Literal\n\n\
CONTRACT_VERSION = \"{}\"\n\
MCP_PROTOCOL_VERSION = \"{}\"\n\n",
        manifest.contract_version, manifest.mcp_protocol_version
    );
    for tool in &manifest.tools {
        let class_name = format!("{}Args", pascal_case(&tool.name));
        let properties = ordered_properties(tool)?;
        let required = required_set(tool);
        writeln!(out, "@dataclass(frozen=True)").unwrap();
        writeln!(out, "class {class_name}:").unwrap();
        for (name, schema) in properties
            .iter()
            .filter(|(name, _)| required.contains(name))
        {
            writeln!(out, "    {name}: {}", python_type(schema)).unwrap();
        }
        for (name, schema) in properties
            .iter()
            .filter(|(name, _)| !required.contains(name))
        {
            writeln!(out, "    {name}: {} | None = None", python_type(schema)).unwrap();
        }
        writeln!(out, "\n    def to_arguments(self) -> dict[str, Any]:").unwrap();
        writeln!(out, "        arguments: dict[str, Any] = {{}}").unwrap();
        for (name, _) in &properties {
            if required.contains(name) {
                writeln!(out, "        arguments[\"{name}\"] = self.{name}").unwrap();
            } else {
                writeln!(out, "        if self.{name} is not None:").unwrap();
                writeln!(out, "            arguments[\"{name}\"] = self.{name}").unwrap();
            }
        }
        writeln!(out, "        return arguments\n\n").unwrap();
    }
    out.push_str("class GeneratedDriverMixin:\n");
    out.push_str("    def call_tool(self, name: str, arguments: dict[str, Any]) -> Any:\n");
    out.push_str("        raise NotImplementedError\n\n");
    for tool in &manifest.tools {
        let class_name = format!("{}Args", pascal_case(&tool.name));
        writeln!(
            out,
            "    def {}(self, arguments: {class_name}) -> Any:\n        return self.call_tool(\"{}\", arguments.to_arguments())\n",
            tool.name, tool.name
        )
        .unwrap();
    }
    out.push_str("\nclass GeneratedAsyncDriverMixin:\n");
    out.push_str("    async def call_tool(self, name: str, arguments: dict[str, Any]) -> Any:\n");
    out.push_str("        raise NotImplementedError\n\n");
    for tool in &manifest.tools {
        let class_name = format!("{}Args", pascal_case(&tool.name));
        writeln!(
            out,
            "    async def {}(self, arguments: {class_name}) -> Any:\n        return await self.call_tool(\"{}\", arguments.to_arguments())\n",
            tool.name, tool.name
        )
        .unwrap();
    }
    normalize_eof(&mut out);
    Ok(out)
}

fn generate_typescript(manifest: &ContractManifest) -> Result<String, String> {
    let mut out = format!(
        "// Generated by cua-contract-gen; do not edit by hand.\n\n\
import type {{ ToolResult }} from \"./result.js\"\n\n\
export const CONTRACT_VERSION = \"{}\" as const\n\
export const MCP_PROTOCOL_VERSION = \"{}\" as const\n\n",
        manifest.contract_version, manifest.mcp_protocol_version
    );
    for tool in &manifest.tools {
        let type_name = format!("{}Args", pascal_case(&tool.name));
        let properties = ordered_properties(tool)?;
        let required = required_set(tool);
        writeln!(out, "export interface {type_name} {{").unwrap();
        for (wire_name, schema) in &properties {
            let name = camel_case(wire_name);
            let optional = if required.contains(wire_name) {
                ""
            } else {
                "?"
            };
            writeln!(out, "  {name}{optional}: {}", typescript_type(schema)).unwrap();
        }
        writeln!(out, "}}\n").unwrap();
        writeln!(
            out,
            "function {}ToWire(input: {type_name}): Record<string, unknown> {{",
            camel_case(&tool.name)
        )
        .unwrap();
        writeln!(out, "  const output: Record<string, unknown> = {{}}").unwrap();
        for (wire_name, _) in &properties {
            let name = camel_case(wire_name);
            if required.contains(wire_name) {
                writeln!(out, "  output.{wire_name} = input.{name}").unwrap();
            } else {
                writeln!(
                    out,
                    "  if (input.{name} !== undefined) output.{wire_name} = input.{name}"
                )
                .unwrap();
            }
        }
        writeln!(out, "  return output\n}}\n").unwrap();
    }
    out.push_str("export abstract class GeneratedDriver {\n");
    out.push_str("  abstract callTool(name: string, input?: Record<string, unknown>): Promise<ToolResult>\n\n");
    for tool in &manifest.tools {
        let type_name = format!("{}Args", pascal_case(&tool.name));
        writeln!(
            out,
            "  {}(input: {type_name}): Promise<ToolResult> {{\n    return this.callTool(\"{}\", {}ToWire(input))\n  }}\n",
            camel_case(&tool.name),
            tool.name,
            camel_case(&tool.name)
        )
        .unwrap();
    }
    out.push_str("}\n");
    normalize_eof(&mut out);
    Ok(out)
}

fn normalize_eof(output: &mut String) {
    let content_len = output.trim_end().len();
    output.truncate(content_len);
    output.push('\n');
}

fn ordered_properties(tool: &ToolContract) -> Result<Vec<(String, Value)>, String> {
    let properties = tool.input_schema["properties"]
        .as_object()
        .ok_or_else(|| format!("{}.input_schema.properties is not an object", tool.name))?;
    let mut ordered = Vec::new();
    for name in required_names(tool) {
        if let Some(schema) = properties.get(&name) {
            ordered.push((name, schema.clone()));
        }
    }
    let required = required_set(tool);
    ordered.extend(
        properties
            .iter()
            .filter(|(name, _)| !required.contains(*name))
            .map(|(name, schema)| (name.clone(), schema.clone())),
    );
    Ok(ordered)
}

fn required_names(tool: &ToolContract) -> Vec<String> {
    tool.input_schema["required"]
        .as_array()
        .into_iter()
        .flatten()
        .filter_map(Value::as_str)
        .map(str::to_owned)
        .collect()
}

fn required_set(tool: &ToolContract) -> BTreeSet<String> {
    required_names(tool).into_iter().collect()
}

fn python_type(schema: &Value) -> String {
    if let Some(value) = schema["const"].as_str() {
        return format!("Literal[\"{value}\"]");
    }
    if let Some(values) = schema["enum"].as_array() {
        let values = values
            .iter()
            .filter_map(Value::as_str)
            .map(|value| format!("\"{value}\""))
            .collect::<Vec<_>>()
            .join(", ");
        return format!("Literal[{values}]");
    }
    match schema["type"].as_str() {
        Some("string") => "str".into(),
        Some("integer") => "int".into(),
        Some("number") => "float".into(),
        Some("boolean") => "bool".into(),
        Some("array") => format!("list[{}]", python_type(&schema["items"])),
        _ => "dict[str, Any]".into(),
    }
}

fn typescript_type(schema: &Value) -> String {
    if let Some(value) = schema["const"].as_str() {
        return format!("\"{value}\"");
    }
    if let Some(values) = schema["enum"].as_array() {
        return values
            .iter()
            .filter_map(Value::as_str)
            .map(|value| format!("\"{value}\""))
            .collect::<Vec<_>>()
            .join(" | ");
    }
    match schema["type"].as_str() {
        Some("string") => "string".into(),
        Some("integer" | "number") => "number".into(),
        Some("boolean") => "boolean".into(),
        Some("array") => format!("{}[]", typescript_type(&schema["items"])),
        _ => "Record<string, unknown>".into(),
    }
}

fn pascal_case(value: &str) -> String {
    value
        .split('_')
        .map(|part| {
            let mut chars = part.chars();
            chars
                .next()
                .map(|first| first.to_uppercase().collect::<String>() + chars.as_str())
                .unwrap_or_default()
        })
        .collect()
}

fn camel_case(value: &str) -> String {
    let pascal = pascal_case(value);
    let mut chars = pascal.chars();
    chars
        .next()
        .map(|first| first.to_lowercase().collect::<String>() + chars.as_str())
        .unwrap_or_default()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn generated_paths_reject_escape_components() {
        assert!(validate_relative(Path::new("src/generated.ts")).is_ok());
        assert!(validate_relative(Path::new("../generated.ts")).is_err());
        assert!(validate_relative(Path::new("/tmp/generated.ts")).is_err());
    }

    #[test]
    fn ownership_inventory_is_sorted_and_portable() {
        let paths = [PathBuf::from("z.ts"), PathBuf::from("src/a.ts")]
            .into_iter()
            .collect::<BTreeSet<_>>();
        assert_eq!(
            ownership_contents(paths.iter()),
            "# Generated by cua-contract-gen; one owned path per line.\nsrc/a.ts\nz.ts\n"
        );
    }

    #[test]
    fn check_rejects_stale_owned_files() {
        let root = std::env::temp_dir().join(format!(
            "cua-contract-gen-stale-owned-{}",
            std::process::id()
        ));
        fs::create_dir_all(&root).expect("create test directory");
        fs::write(root.join(OWNERSHIP_FILE), "stale.py\n").expect("write inventory");
        let group = OwnershipGroup {
            root: root.clone(),
            files: BTreeMap::from([
                (
                    PathBuf::from(OWNERSHIP_FILE),
                    ownership_contents([&PathBuf::from("generated.py")].into_iter()),
                ),
                (PathBuf::from("generated.py"), "generated\n".into()),
            ]),
        };
        let error = apply_group(&group, true).expect_err("stale path must fail check");
        assert!(error.contains("stale.py"), "{error}");
        fs::remove_file(root.join(OWNERSHIP_FILE)).expect("remove inventory");
        fs::remove_dir(root).expect("remove test directory");
    }
}
